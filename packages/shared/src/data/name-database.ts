// ============================================
// LOOKUP HELPERS
// ============================================
// These functions provide the lookup/normalization layer used by phi-filter.ts.
// They operate on the raw arrays above.

const FIRST_NAME_SET: Set<string> = new Set(RAW_FIRST_NAMES);
const LAST_NAME_SET: Set<string> = new Set(RAW_LAST_NAMES);

/**
 * Normalize a candidate token for name lookup.
 * Strips punctuation, possessives, and lowercases.
 * Returns empty string for tokens that should be skipped.
 */
export function normalizeCandidateToken(token: string): string {
  return token
    .replace(/[.,!?;:'"()[\]{}]/g, "")
    .replace(/'s$/i, "")
    .trim()
    .toLowerCase();
}

/**
 * Check if a normalized token is a known first name.
 */
export function isKnownFirstName(token: string): boolean {
  return FIRST_NAME_SET.has(token.toLowerCase());
}

/**
 * Check if a normalized token is a known last name.
 */
export function isKnownLastName(token: string): boolean {
  return LAST_NAME_SET.has(token.toLowerCase());
}

